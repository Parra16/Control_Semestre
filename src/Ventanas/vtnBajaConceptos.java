    public void cargaComboLetras() {
        try {
            jcletters.removeAllItems();
            Nodo aux = VtnPrincipal.raiz.getSiguiente();
            do {
                jcletters.addItem(aux.getEtiqueta());
                aux = aux.getSiguiente();
            } while (aux != VtnPrincipal.raiz.getSiguiente());

            if (jcletters.getItemCount() == 0) {
                Mensaje.error(this, "there are no letters in alphabet");
                this.dispose();
            } else {
                jcletters.setSelectedIndex(0);

            }
        } catch (Exception e) {
            Mensaje.error(this, "there are no letters in alphabet");
            System.out.println("Error.." + e.toString());
        }
    }

    public void cargaComboConceptos() {

        String s[] = new String[1];
        s[0] = String.valueOf(jcletters.getSelectedItem());
        System.out.println(jcletters.getSelectedItem());
        try {
            txtword.removeAllItems();
            //Nodo aux = VtnPrincipal.raiz.getSiguiente();
            Nodo aux = ManipulaListas.busca(VtnPrincipal.raiz, s, 0).getAbajo();
            Nodo princi = aux;
            do {
                System.out.println("Etiqueta" + aux.getEtiqueta());
                txtword.addItem(aux.getEtiqueta());
                aux = aux.getSiguiente();
            } while (aux != princi);

            if (txtword.getItemCount() == 0) {
                Mensaje.error(this, "al chile esta fallando");
                
            } else {
                txtword.setSelectedIndex(0);
            }
        } catch (Exception e) {
            Mensaje.error(this, "there are no concepts ");
            System.out.println("Error.." + e.toString());
        }
    }

